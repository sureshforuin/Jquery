function formChartData(DATA, changeIt = false) {
    var newArray = [];
    for (var i = 0; i < DATA.length; i++) {
        var value = DATA[i];
        var tempVal = changeIt ? value[1] * Math.random() - 100 : value[1];

        newArray.push([
            Date.UTC(value[0].toString().substring(0,4), value[0].toString().substring(6,4)),
            tempVal
          ]);
    }
    return newArray;
}


    
